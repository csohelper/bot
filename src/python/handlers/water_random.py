# Хэндлер ловит всё, что НАЧИНАЕТСЯ с нужной фразы
@router.message(
    lambda message: message.text and(
        message.text.lower().strip().startswith("цсошник, вода будет") or
        message.text.lower().strip().startswith("цсошник вода будет") or
        message.text.lower().strip().startswith("ботяра вода будет")
    )
)
async def command_water_handler(message: Message) -> None:
    initial_text = random.choice(THINKING_PHRASES)

    reply_ = await message.reply(f"⏳ {initial_text}")
    await asyncio.sleep(1.2)

    # Вторая фаза размышлений (берем другую фразу)
    second_thinking = random.choice(
        [p for p in THINKING_PHRASES if p not in initial_text]
    )
    await reply_.edit_text(f"⚙️ {second_thinking}")
    await asyncio.sleep(1.2)

    # 15% на "Да", 85% на суровое "Нет" (2 месяца без воды — это не шутки)
    if random.choices([True, False], weights=[15, 85])[0]:
        final_answer = random.choice(ANSWERS_YES)
    else:
        final_answer = random.choice(ANSWERS_NO)

    await reply_.edit_text(final_answer)
